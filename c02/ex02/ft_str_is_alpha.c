int	ft_str_is_alpha(char *str)
{
	int i;
	int ret;

	ret = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if ((65 <= str[i] && str[i] <= 90) || (97 <= str[i] && str[i] <= 122))
			ret = 0;
		else
		{
			ret = 1;
			break ;
		}
		i++;
	}
	return (ret);
}
